"""¿Qué es el módulo math?
El módulo 'math' en Python proporciona funciones matemáticas estándar como raíces cuadradas,
funciones trigonométricas, logaritmos, constantes como pi, y más.
Es parte de la biblioteca estándar, por lo que no necesitas instalar nada adicional."""

# Importar el módulo math
import math

# Utilizar un elemento del módulo math
  # Ejemplo: Calcular la raíz cuadrada de un número
numero = 25
raiz_cuadrada = math.sqrt(numero)

# Mostrar el resultado
print(f"La raíz cuadrada de {numero} es {raiz_cuadrada}")

# Otro ejemplo: Usar la constante pi
print(f"El valor de pi es aproximadamente: {math.pi}")
